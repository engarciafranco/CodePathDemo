import UIKit

extension UIColor {
    struct CustomColor {
        struct Red {
            static let spanishCrimsom = UIColor(netHex: 0xE01A4F)
            static let jellyBean = UIColor(netHex: 0xD96161)
            
        }
        
        struct Yellow {
            static let deepLemon = UIColor(netHex: 0xF3B61F)
            static let mellowYellow = UIColor(netHex: 0xF7D488)
            static let sandyBrown = UIColor(netHex: 0xF9A364)
        }
        
        struct Green {
            static let aeroBlue = UIColor(netHex: 0xD3FAD6)
            static let midAquarium = UIColor(netHex: 0x61D9BC)
        }
        
        struct Blue {
            static let veryLightAzure = UIColor(netHex: 0x69B8F2)
            static let spaceCadet = UIColor(netHex: 0x203858)
        }
        
        struct Violet {
             static let midRedPurple = UIColor(netHex: 0x270722)
        }
        
    }
    
}
